import { Card, CardContent } from "@/components/ui/card";
import { Button } from "@/components/ui/button";
import { Badge } from "@/components/ui/badge";
import { Progress } from "@/components/ui/progress";
import { 
  TrendingUp, 
  CheckCircle, 
  AlertCircle, 
  AlertTriangle, 
  Lightbulb, 
  Download, 
  RotateCcw,
  Copy 
} from "lucide-react";
import { jsPDF } from "jspdf";
import type { ATSAnalysis } from "@shared/schema";

interface ResultsDashboardProps {
  results: ATSAnalysis;
  onNewAnalysis: () => void;
  "data-testid"?: string;
}

export function ResultsDashboard({ results, onNewAnalysis, "data-testid": testId }: ResultsDashboardProps) {
  const getScoreColor = (score: number) => {
    if (score >= 81) return "text-green-600";
    if (score >= 41) return "text-yellow-600";
    return "text-red-600";
  };

  const getScoreLabel = (score: number) => {
    if (score >= 81) return "Excellent";
    if (score >= 41) return "Good";
    return "Poor";
  };

  const copyToClipboard = async (text: string) => {
    try {
      await navigator.clipboard.writeText(text);
    } catch (err) {
      console.error('Failed to copy text: ', err);
    }
  };

  const exportToPDF = () => {
    const doc = new jsPDF();
    const pageHeight = doc.internal.pageSize.height;
    let yPos = 20;
    
    // Title
    doc.setFontSize(18);
    doc.setFont('helvetica', 'bold');
    doc.text('ATS Resume Analysis Report', 20, yPos);
    yPos += 20;
    
    // ATS Score
    doc.setFontSize(14);
    doc.setFont('helvetica', 'bold');
    doc.text(`ATS Compatibility Score: ${results.ats_score}/100`, 20, yPos);
    yPos += 10;
    
    const scoreLabel = getScoreLabel(results.ats_score);
    doc.setFontSize(12);
    doc.setFont('helvetica', 'normal');
    doc.text(`Rating: ${scoreLabel}`, 20, yPos);
    yPos += 20;
    
    // Skills Matched
    if (results.skills_matched.length > 0) {
      doc.setFontSize(14);
      doc.setFont('helvetica', 'bold');
      doc.text('Skills Matched:', 20, yPos);
      yPos += 10;
      
      doc.setFontSize(10);
      doc.setFont('helvetica', 'normal');
      results.skills_matched.forEach((skill, index) => {
        if (yPos > pageHeight - 20) {
          doc.addPage();
          yPos = 20;
        }
        doc.text(`â€¢ ${skill}`, 25, yPos);
        yPos += 6;
      });
      yPos += 10;
    }
    
    // Missing Keywords
    if (results.missing_keywords.length > 0) {
      if (yPos > pageHeight - 40) {
        doc.addPage();
        yPos = 20;
      }
      
      doc.setFontSize(14);
      doc.setFont('helvetica', 'bold');
      doc.text('Missing Keywords:', 20, yPos);
      yPos += 10;
      
      doc.setFontSize(10);
      doc.setFont('helvetica', 'normal');
      results.missing_keywords.forEach((keyword, index) => {
        if (yPos > pageHeight - 20) {
          doc.addPage();
          yPos = 20;
        }
        doc.text(`â€¢ ${keyword}`, 25, yPos);
        yPos += 6;
      });
      yPos += 10;
    }
    
    // Formatting Issues
    if (results.formatting_issues.length > 0) {
      if (yPos > pageHeight - 40) {
        doc.addPage();
        yPos = 20;
      }
      
      doc.setFontSize(14);
      doc.setFont('helvetica', 'bold');
      doc.text('Formatting Issues:', 20, yPos);
      yPos += 10;
      
      doc.setFontSize(10);
      doc.setFont('helvetica', 'normal');
      results.formatting_issues.forEach((issue, index) => {
        if (yPos > pageHeight - 20) {
          doc.addPage();
          yPos = 20;
        }
        doc.text(`â€¢ ${issue}`, 25, yPos);
        yPos += 6;
      });
      yPos += 10;
    }
    
    // Suggestions
    if (results.suggestions.length > 0) {
      if (yPos > pageHeight - 40) {
        doc.addPage();
        yPos = 20;
      }
      
      doc.setFontSize(14);
      doc.setFont('helvetica', 'bold');
      doc.text('Improvement Suggestions:', 20, yPos);
      yPos += 10;
      
      doc.setFontSize(10);
      doc.setFont('helvetica', 'normal');
      results.suggestions.forEach((suggestion, index) => {
        if (yPos > pageHeight - 20) {
          doc.addPage();
          yPos = 20;
        }
        doc.text(`${index + 1}. ${suggestion}`, 25, yPos);
        yPos += 8;
      });
    }
    
    // Footer
    const totalPages = doc.internal.pages.length - 1;
    for (let i = 1; i <= totalPages; i++) {
      doc.setPage(i);
      doc.setFontSize(8);
      doc.setFont('helvetica', 'normal');
      doc.text(`Generated by ATS Score Finder - Page ${i} of ${totalPages}`, 20, pageHeight - 10);
      doc.text(`Report generated on: ${new Date().toLocaleDateString()}`, 120, pageHeight - 10);
    }
    
    // Save the PDF
    const fileName = `ats-analysis-report-${new Date().toISOString().split('T')[0]}.pdf`;
    doc.save(fileName);
  };

  return (
    <div className="space-y-6 animate-in fade-in duration-500" data-testid={testId}>
      <Card className="shadow-sm">
        <CardContent className="p-6">
          <h2 className="text-xl font-semibold text-foreground mb-6 flex items-center">
            <TrendingUp className="text-primary mr-2 h-5 w-5" />
            ATS Score Analysis
          </h2>
          
          <div className="grid md:grid-cols-2 gap-6">
            <div className="space-y-4">
              <div className="text-center">
                <div 
                  className={`text-4xl font-bold mb-2 ${getScoreColor(results.ats_score)}`}
                  data-testid="text-ats-score"
                >
                  {results.ats_score}%
                </div>
                <p className="text-muted-foreground text-sm">ATS Compatibility Score</p>
              </div>
              
              <Progress 
                value={results.ats_score} 
                className="w-full h-3"
                data-testid="progress-ats-score"
              />
            </div>

            <div className="space-y-4">
              <Card className="bg-muted">
                <CardContent className="p-4">
                  <h4 className="font-medium text-foreground mb-2">Score Category</h4>
                  <div className="text-center">
                    <Badge 
                      variant={results.ats_score >= 81 ? "default" : results.ats_score >= 41 ? "secondary" : "destructive"}
                      className="text-lg px-4 py-2"
                      data-testid="badge-score-category"
                    >
                      {getScoreLabel(results.ats_score)}
                    </Badge>
                  </div>
                </CardContent>
              </Card>
            </div>
          </div>
        </CardContent>
      </Card>

      <div className="grid lg:grid-cols-2 gap-6">
        <Card className="shadow-sm">
          <CardContent className="p-6">
            <h3 className="text-lg font-semibold text-foreground mb-4 flex items-center">
              <CheckCircle className="text-green-600 mr-2 h-5 w-5" />
              Skills Matched ({results.skills_matched.length})
            </h3>
            
            <div className="flex flex-wrap gap-2" data-testid="container-matched-skills">
              {results.skills_matched.map((skill, index) => (
                <Badge 
                  key={index}
                  variant="secondary" 
                  className="bg-green-100 text-green-800"
                >
                  {skill}
                </Badge>
              ))}
              {results.skills_matched.length === 0 && (
                <p className="text-muted-foreground text-sm">No matched skills found</p>
              )}
            </div>
          </CardContent>
        </Card>

        <Card className="shadow-sm border-orange-200">
          <CardContent className="p-6">
            <h3 className="text-lg font-semibold text-foreground mb-4 flex items-center">
              <AlertCircle className="text-orange-600 mr-2 h-5 w-5" />
              Missing Keywords ({results.missing_keywords.length})
            </h3>
            
            <div className="flex flex-wrap gap-2" data-testid="container-missing-keywords">
              {results.missing_keywords.map((keyword, index) => (
                <Badge 
                  key={index}
                  variant="outline" 
                  className="border-orange-300 text-orange-800 hover:bg-orange-100 cursor-pointer"
                  onClick={() => copyToClipboard(keyword)}
                  title={`Click to copy "${keyword}"`}
                >
                  {keyword}
                </Badge>
              ))}
              {results.missing_keywords.length === 0 && (
                <p className="text-green-800 text-sm font-medium">No critical keywords missing!</p>
              )}
            </div>
          </CardContent>
        </Card>
      </div>

      <Card className="shadow-sm">
        <CardContent className="p-6">
          <h3 className="text-lg font-semibold text-foreground mb-4 flex items-center">
            <AlertTriangle className="text-yellow-600 mr-2 h-5 w-5" />
            Formatting Issues ({results.formatting_issues.length})
          </h3>
          
          <div className="space-y-3">
            {results.formatting_issues.map((issue, index) => (
              <div key={index} className="flex items-start p-3 bg-yellow-50 border border-yellow-200 rounded-lg">
                <AlertTriangle className="text-yellow-600 mr-3 h-4 w-4 flex-shrink-0 mt-0.5" />
                <p className="text-yellow-800 text-sm">{issue}</p>
              </div>
            ))}
            {results.formatting_issues.length === 0 && (
              <p className="text-muted-foreground text-sm">No formatting issues detected</p>
            )}
          </div>
        </CardContent>
      </Card>

      <Card className="shadow-sm border-2 border-primary/20">
        <CardContent className="p-6">
          <h3 className="text-lg font-semibold text-foreground mb-4 flex items-center">
            <Lightbulb className="text-primary mr-2 h-5 w-5" />
            ðŸ¤– AI-Powered Recommendations
          </h3>
          
          <div className="space-y-4">
            {results.suggestions.map((suggestion, index) => (
              <Card key={index} className="border bg-gradient-to-r from-background to-primary/5">
                <CardContent className="p-4">
                  <div className="flex items-start space-x-3">
                    <div className="w-6 h-6 rounded-full bg-primary/10 flex items-center justify-center flex-shrink-0 mt-1">
                      <span className="text-primary text-xs font-medium">{index + 1}</span>
                    </div>
                    <p className="text-foreground text-sm leading-relaxed">{suggestion}</p>
                  </div>
                </CardContent>
              </Card>
            ))}
            {results.suggestions.length === 0 && (
              <div className="text-center p-8 bg-muted/50 rounded-lg">
                <Lightbulb className="mx-auto h-12 w-12 text-muted-foreground mb-4" />
                <p className="text-muted-foreground">Great job! Your resume looks excellent.</p>
              </div>
            )}
          </div>
        </CardContent>
      </Card>

      <div className="flex flex-wrap gap-4 justify-center">
        <Button onClick={exportToPDF} className="flex items-center space-x-2">
          <Download className="h-4 w-4" />
          <span>Export Report</span>
        </Button>
        <Button variant="secondary" onClick={onNewAnalysis} className="flex items-center space-x-2">
          <RotateCcw className="h-4 w-4" />
          <span>Analyze Another Resume</span>
        </Button>
      </div>
    </div>
  );
}